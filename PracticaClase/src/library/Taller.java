package library;

public class Taller {
    String especialidad;
    String horario;
    int personal;
    String direccion;
    String dueno;

    public Taller(String especialidad, String horario, int personal, String direccion, String dueno) {
        this.especialidad = especialidad;
        this.horario = horario;
        this.personal = personal;
        this.direccion = direccion;
        this.dueno = dueno;
    }
}
