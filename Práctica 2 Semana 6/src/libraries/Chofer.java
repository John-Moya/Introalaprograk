package libraries;

public class Chofer {

    String nombre;
    int edad;
    int AnosEmpresa;

    public Chofer(){
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getAnosEmpresa() {
        return AnosEmpresa;
    }

    public void setAnosEmpresa(int anosEmpresa) {
        AnosEmpresa = anosEmpresa;
    }
}
